package Operadores;

public class Comparacao {
    public static void main (String[] args){

        int valor1, valor2;

        valor1 = 10;
        valor2 = 5;

        System.out.println(valor1 == valor2); // Está comparando se é igual
        System.out.println(valor1 != valor2); // Está comparando se é diferente
        System.out.println(valor1 >= valor2); // Está comparando se é maior  ou igual
        System.out.println(valor1 <= valor2); // Ésta comparando se é menor  ou igual
        System.out.println(valor1 > valor2);  // Está comparando se é maior
        System.out.println(valor1 < valor2);  // Ésta comparando se é menor
        
    }
}
